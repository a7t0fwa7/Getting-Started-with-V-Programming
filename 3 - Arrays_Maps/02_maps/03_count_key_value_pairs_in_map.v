mut student_1 := {
	'english':     90
	'mathematics': 96
	'physics':     83
	'chemistry':   89
}
cnt := student_1.len
println('There are $cnt key-value pairs in student_1 map') // There are 4 key-value pairs in student_1 map

/*
You can check the number of key-value pairs present in a map using len, which is a read-only property available on the map variable.
